// @ts-nocheck
import { MinusIcon } from '@/components/icons'
import { cn } from '@/lib/utils'
import { OTPInput, OTPInputContext } from 'input-otp'
import { useContext, type ComponentProps } from 'react'

function InputOTP({
	className,
	containerClassName,
	...props
}: ComponentProps<typeof OTPInput> & {
	containerClassName?: string
}) {
	return (
		<OTPInput
			data-slot='input-otp'
			containerClassName={cn('cn-input-otp flex items-center has-disabled:opacity-50', containerClassName)}
			spellCheck={false}
			className={cn('disabled:cursor-not-allowed', className)}
			{...props}
		/>
	)
}

function InputOTPGroup({ className, ...props }: ComponentProps<'div'>) {
	return (
		<div
			data-slot='input-otp-group'
			className={cn(
				'flex items-center rounded-md has-aria-invalid:border-destructive has-aria-invalid:ring-[2px] has-aria-invalid:ring-destructive/20 dark:has-aria-invalid:ring-destructive/40',
				className,
			)}
			{...props}
		/>
	)
}

function InputOTPSlot({
	index,
	className,
	...props
}: ComponentProps<'div'> & {
	index: number
}) {
	const inputOTPContext = useContext(OTPInputContext)
	const { char, hasFakeCaret, isActive } = inputOTPContext?.slots[index] ?? {}

	return (
		<div
			data-slot='input-otp-slot'
			data-active={isActive}
			className={cn(
				'relative flex size-7 items-center justify-center border-y border-r border-input bg-input/20 text-xs/relaxed transition-all outline-none first:rounded-l-md first:border-l last:rounded-r-md aria-invalid:border-destructive data-[active=true]:z-10 data-[active=true]:border-ring data-[active=true]:ring-[2px] data-[active=true]:ring-ring/30 data-[active=true]:aria-invalid:border-destructive data-[active=true]:aria-invalid:ring-destructive/20 dark:bg-input/30 dark:data-[active=true]:aria-invalid:ring-destructive/40',
				className,
			)}
			{...props}
		>
			{char}
			{hasFakeCaret && (
				<div className='pointer-events-none absolute inset-0 flex items-center justify-center'>
					<div className='h-4 w-px animate-caret-blink bg-foreground duration-1000' />
				</div>
			)}
		</div>
	)
}

function InputOTPSeparator({ ...props }: ComponentProps<'div'>) {
	return (
		<div
			data-slot='input-otp-separator'
			className="flex items-center [&_svg:not([class*='size-'])]:size-4"
			role='separator'
			{...props}
		>
			<MinusIcon className='size-4' />
		</div>
	)
}

export { InputOTP, InputOTPGroup, InputOTPSlot, InputOTPSeparator }
